// Helper function to get menu item description based on language
export const getMenuDescription = (itemKey, language) => {
  if (language === 'fr') {
    return ''; // French descriptions are already in data.js
  }
  return menuTranslations[language]?.[itemKey] || '';
};

// Helper function to get menu item tags translation based on language
export const getMenuTags = (tags, language) => {
  if (language === 'fr' || !tags) {
    return tags; // French tags are already in data.js
  }
  return tagsTranslations[language]?.[tags] || tags;
};

// Helper function to get menu item title translation based on language
export const getMenuTitle = (title, language) => {
  if (language === 'fr' || !title) {
    return title; // French titles are already in data.js
  }
  return titleTranslations[language]?.[title] || title;
};
